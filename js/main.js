/* ============================================================
   THEME
   ============================================================ */
const html = document.documentElement;
html.setAttribute('data-theme', localStorage.getItem('theme') || 'light');

document.addEventListener('click', e => {
  if (e.target.closest('#themeToggle')) {
    const next = html.getAttribute('data-theme') === 'dark' ? 'light' : 'dark';
    html.setAttribute('data-theme', next);
    localStorage.setItem('theme', next);
  }
});

/* ============================================================
   i18n
   ============================================================ */
let currentLang = localStorage.getItem('lang') || 'en';

function applyTranslations(lang) {
  const t = LOCALES[lang] || LOCALES['en'];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t[el.getAttribute('data-i18n')];
    if (val !== undefined) el.textContent = val;
  });
  html.setAttribute('lang', lang === 'zh-cn' ? 'zh-CN' : 'en');
  const enEl = document.getElementById('langEn');
  const zhEl = document.getElementById('langZh');
  if (enEl) enEl.classList.toggle('active', lang === 'en');
  if (zhEl) zhEl.classList.toggle('active', lang === 'zh-cn');
}

document.addEventListener('click', e => {
  const opt = e.target.closest('.lang-option');
  if (!opt) return;
  const lang = opt.id === 'langZh' ? 'zh-cn' : 'en';
  if (lang !== currentLang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    applyTranslations(lang);
  }
});

applyTranslations(currentLang);

/* ============================================================
   MOBILE MENU
   ============================================================ */
document.addEventListener('click', e => {
  const hamburger = document.getElementById('hamburger');
  const navMenu   = document.getElementById('navMenu');
  if (!hamburger || !navMenu) return;

  if (e.target.closest('#hamburger')) {
    const open = navMenu.classList.toggle('open');
    hamburger.classList.toggle('active', open);
    hamburger.setAttribute('aria-expanded', String(open));
    return;
  }
  if (e.target.closest('.nav-link, .nav-btn')) {
    navMenu.classList.remove('open');
    hamburger.classList.remove('active');
    hamburger.setAttribute('aria-expanded', 'false');
  }
});

